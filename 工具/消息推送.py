# ==================== 基础发送函数 ====================
def 发送飞书消息(内容: str, 消息类型: str = "info", 标题: str = None) -> bool:
    """
    发送消息到飞书群
    
    参数:
        内容: 消息内容
        消息类型: info/warning/success/error/trade/signal/buy/sell/risk/system/report
        标题: 可选的消息标题
    
    返回:
        是否发送成功
    """
    global 上次发送时间
    
    if not FEISHU_WEBHOOK:
        print("⚠️ 未配置飞书 Webhook，跳过消息推送")
        return False
    
    # 限流：防止发送过快
    当前时间 = time.time()
    if 当前时间 - 上次发送时间 < 最小发送间隔:
        time.sleep(最小发送间隔 - (当前时间 - 上次发送时间))
    
    # 获取图标
    图标 = 图标映射.get(消息类型, '📊')
    
    # 格式化消息
    if 标题:
        完整标题 = f"{图标} {标题}"
    else:
        完整标题 = f"{图标} 量化交易系统通知"
    
    # 使用富文本格式（post消息）
    message = {
        "msg_type": "post",
        "content": {
            "post": {
                "zh_cn": {
                    "title": 完整标题,
                    "content": [
                        [{"tag": "text", "text": 内容}],
                        [{"tag": "text", "text": f"\n\n⏰ 时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}"}]
                    ]
                }
            }
        }
    }
    
    try:
        response = requests.post(
            FEISHU_WEBHOOK,
            headers={'Content-Type': 'application/json'},
            data=json.dumps(message),
            timeout=5
        )
        
        上次发送时间 = time.time()
        
        if response.status_code == 200:
            result = response.json()
            if result.get('code') == 0:
                print(f"✅ 飞书消息已发送 [{消息类型}]")
                return True
            else:
                print(f"❌ 飞书API返回错误: {result}")
                return False
        else:
            print(f"❌ 发送失败 HTTP {response.status_code}: {response.text}")
            return False
            
    except requests.exceptions.Timeout:
        print("❌ 飞书消息发送超时")
        return False
    except Exception as e:
        print(f"❌ 发送异常: {e}")
        return False


def 发送飞书文本消息(内容: str, 消息类型: str = "info") -> bool:
    """
    发送纯文本消息（简化版）
    
    参数:
        内容: 消息内容
        消息类型: 消息类型
    """
    图标 = 图标映射.get(消息类型, '📊')
    完整内容 = f"{图标} {内容}\n\n时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}"
    
    message = {
        "msg_type": "text",
        "content": {"text": 完整内容}
    }
    
    try:
        response = requests.post(
            FEISHU_WEBHOOK,
            headers={'Content-Type': 'application/json'},
            data=json.dumps(message),
            timeout=5
        )
        return response.status_code == 200
    except Exception as e:
        print(f"发送失败: {e}")
        return False


# ==================== 交易信号通知 ====================
def 发送交易信号(品种: str, 动作: str, 价格: float, 
                 数量: float = None, 理由: str = "", 
                 置信度: float = None) -> bool:
    """
    发送交易信号通知
    
    参数:
        品种: 品种代码
        动作: buy/sell/hold
        价格: 信号价格
        数量: 建议数量
        理由: 信号理由
        置信度: 信号置信度 (0-100)
    """
    动作中文 = {"buy": "买入", "sell": "卖出", "hold": "持有"}.get(动作, 动作)
    消息类型 = 'buy' if 动作 == 'buy' else ('sell' if 动作 == 'sell' else 'signal')
    
    内容 = f"""
📊 【交易信号】
━━━━━━━━━━━━━━━━━━
📈 品种: {品种}
🎯 动作: {动作中文}
💰 价格: {价格:.4f}
"""
    
    if 数量:
        内容 += f"📦 建议数量: {数量:.4f}\n"
    if 置信度:
        置信度图标 = "🟢" if 置信度 >= 70 else ("🟡" if 置信度 >= 50 else "🔴")
        内容 += f"📊 置信度: {置信度图标} {置信度}%\n"
    if 理由:
        内容 += f"📝 理由: {理由}\n"
    
    内容 += "━━━━━━━━━━━━━━━━━━"
    
    return 发送飞书消息(内容, 消息类型, f"{'📈' if 动作 == 'buy' else '📉'} 交易信号 - {品种}")


