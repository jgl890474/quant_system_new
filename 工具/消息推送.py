# ==================== 持仓报告 ====================
def 发送持仓报告(持仓列表: List[Dict], 总资产: float, 
                 可用资金: float, 总盈亏: float) -> bool:
    """
    发送持仓报告
    
    参数:
        持仓列表: 持仓列表，每个元素包含 品种, 数量, 成本, 现价, 盈亏
        总资产: 总资产
        可用资金: 可用资金
        总盈亏: 总盈亏
    """
    内容 = f"""
📋 【持仓报告】
━━━━━━━━━━━━━━━━━━
💰 总资产: {总资产:,.2f}
💵 可用资金: {可用资金:,.2f}
📊 总盈亏: {总盈亏:+,.2f}
━━━━━━━━━━━━━━━━━━

📦 持仓明细:
"""
    
    for pos in 持仓列表:
        品种 = pos.get('品种', '')
        数量 = pos.get('数量', 0)
        成本 = pos.get('成本', 0)
        现价 = pos.get('现价', 0)
        盈亏 = pos.get('盈亏', 0)
        盈亏率 = (盈亏 / (成本 * 数量)) * 100 if 成本 > 0 and 数量 > 0 else 0
        
        盈亏图标 = "🟢" if 盈亏 >= 0 else "🔴"
        
        内容 += f"\n  {品种}"
        内容 += f"\n    数量: {数量:.4f} | 成本: {成本:.2f}"
        内容 += f"\n    现价: {现价:.2f} | 盈亏: {盈亏图标} {盈亏:+.2f} ({盈亏率:+.1f}%)"
    
    内容 += "\n━━━━━━━━━━━━━━━━━━"
    
    return 发送飞书消息(内容, 'report', "持仓报告")


