# -*- coding: utf-8 -*-
import pandas as pd
import numpy as np
from datetime import datetime
from .历史数据获取 import 获取历史K线
from .订单模拟 import 订单簿
from .评价指标 import 计算完整指标


class 回测引擎:
    """完整回测引擎"""
    
    def __init__(self, 初始资金=100000, 滑点基点=0.0001, 手续费率=0.0005):
        self.初始资金 = 初始资金
        self.当前资金 = 初始资金
        self.持仓 = {}  # 品种 -> 数量
        self.持仓成本 = {}  # 品种 -> 平均成本
        self.交易记录 = []
        self.每日净值 = []
        self.订单簿 = None
        self.滑点基点 = 滑点基点
        self.手续费率 = 手续费率
    
    def 运行回测(self, 策略, 品种, 开始日期=None, 结束日期=None, 周期="1d"):
        """
        运行完整回测
        
        参数:
        - 策略: 策略实例
        - 品种: 交易品种
        - 开始日期: YYYY-MM-DD
        - 结束日期: YYYY-MM-DD
        - 周期: K线周期
        
        返回: 回测结果字典
        """
        
        # 1. 获取历史数据
        print(f"📊 获取 {品种} 历史数据...")
        历史数据 = 获取历史K线(品种, 周期, 开始日期, 结束日期)
        
        if 历史数据.empty:
            return {"错误": "无法获取历史数据"}
        
        print(f"✅ 获取到 {len(历史数据)} 根K线")
        
        # 2. 初始化
        self.当前资金 = self.初始资金
        self.持仓 = {}
        self.持仓成本 = {}
        self.交易记录 = []
        self.每日净值 = []
        self.订单簿 = 订单簿(self.滑点基点, self.手续费率)
        
        # 3. 逐K线回测
        for 索引, K线 in 历史数据.iterrows():
            当前价格 = K线['收盘']
            当前时间 = 索引
            
            # 3.1 策略信号
            信号 = 策略.处理行情({
                'close': 当前价格,
                'high': K线['最高'],
                'low': K线['最低'],
                'open': K线['开盘'],
                'volume': K线['成交量']
            })
            
            # 3.2 执行交易
            if 信号 == 'buy':
                self._执行买入(品种, 当前价格)
            elif 信号 == 'sell':
                self._执行卖出(品种, 当前价格)
            
            # 3.3 撮合限价单
            成交 = self.订单簿.撮合(品种, 当前价格, 当前时间)
            for 成交单 in 成交:
                if 成交单['订单'].方向 == 'buy':
                    self._记录买入(成交单['订单'].品种, 成交单['成交价'], 成交单['数量'], 成交单['手续费'])
                else:
                    self._记录卖出(成交单['订单'].品种, 成交单['成交价'], 成交单['数量'], 成交单['手续费'])
            
            # 3.4 记录每日净值
            当前净值 = self._计算当前净值(当前价格)
            self.每日净值.append({
                '日期': 当前时间,
                '净值': 当前净值,
                '价格': 当前价格
            })
        
        # 4. 计算评价指标
        最终净值 = self._计算当前净值(历史数据.iloc[-1]['收盘'])
        指标 = 计算完整指标(self.每日净值, self.交易记录, self.初始资金, 最终净值)
        
        # 5. 返回结果
        return {
            "成功": True,
            "初始资金": self.初始资金,
            "最终资金": 最终净值,
            "总收益率": 指标["总收益率"],
            "年化收益率": 指标["年化收益率"],
            "夏普比率": 指标["夏普比率"],
            "最大回撤": 指标["最大回撤"],
            "最大回撤率": 指标["最大回撤率"],
            "胜率": 指标["胜率"],
            "盈亏比": 指标["盈亏比"],
            "交易次数": 指标["交易次数"],
            "盈利次数": 指标["盈利次数"],
            "亏损次数": 指标["亏损次数"],
            "换手率": 指标["换手率"],
            "净值曲线": pd.DataFrame(self.每日净值),
            "交易记录": pd.DataFrame(self.交易记录)
        }
    
    def _执行买入(self, 品种, 价格):
        """执行市价买入"""
        滑点 = 价格 * self.滑点基点
        成交价 = 价格 + 滑点
        数量 = 1000  # 固定数量，可改为动态
        
        手续费 = 成交价 * 数量 * self.手续费率
        
        if 成交价 * 数量 + 手续费 <= self.当前资金:
            self._记录买入(品种, 成交价, 数量, 手续费)
    
    def _执行卖出(self, 品种, 价格):
        """执行市价卖出"""
        滑点 = 价格 * self.滑点基点
        成交价 = 价格 - 滑点
        
        if 品种 in self.持仓 and self.持仓[品种] > 0:
            数量 = 1000
            if 数量 > self.持仓[品种]:
                数量 = self.持仓[品种]
            
            手续费 = 成交价 * 数量 * self.手续费率
            盈亏 = (成交价 - self.持仓成本[品种]) * 数量
            
            self._记录卖出(品种, 成交价, 数量, 手续费, 盈亏)
    
    def _记录买入(self, 品种, 价格, 数量, 手续费):
        """记录买入交易"""
        if 品种 in self.持仓:
            原数量 = self.持仓[品种]
            原成本 = self.持仓成本[品种]
            新数量 = 原数量 + 数量
            新成本 = (原数量 * 原成本 + 数量 * 价格) / 新数量
            self.持仓[品种] = 新数量
            self.持仓成本[品种] = 新成本
        else:
            self.持仓[品种] = 数量
            self.持仓成本[品种] = 价格
        
        self.当前资金 -= (价格 * 数量 + 手续费)
        
        self.交易记录.append({
            '时间': datetime.now(),
            '动作': '买入',
            '品种': 品种,
            '价格': 价格,
            '数量': 数量,
            '手续费': 手续费,
            '盈亏': 0
        })
    
    def _记录卖出(self, 品种, 价格, 数量, 手续费, 盈亏):
        """记录卖出交易"""
        self.持仓[品种] -= 数量
        if self.持仓[品种] <= 0:
            del self.持仓[品种]
            del self.持仓成本[品种]
        
        self.当前资金 += (价格 * 数量 - 手续费)
        
        self.交易记录.append({
            '时间': datetime.now(),
            '动作': '卖出',
            '品种': 品种,
            '价格': 价格,
            '数量': 数量,
            '手续费': 手续费,
            '盈亏': 盈亏
        })
    
    def _计算当前净值(self, 当前价格):
        """计算当前总净值"""
        净值 = self.当前资金
        for 品种, 数量 in self.持仓.items():
            净值 += 数量 * 当前价格
        return 净值
