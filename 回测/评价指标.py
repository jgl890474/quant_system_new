# -*- coding: utf-8 -*-
import numpy as np
import pandas as pd


def 计算夏普比率(收益率序列, 无风险利率=0.02):
    """
    计算夏普比率
    夏普比率 = (收益率均值 - 无风险利率) / 收益率标准差
    """
    if len(收益率序列) < 2:
        return 0
    
    收益率均值 = np.mean(收益率序列)
    收益率标准差 = np.std(收益率序列)
    
    if 收益率标准差 == 0:
        return 0
    
    年化收益率均值 = 收益率均值 * 252  # 假设252个交易日
    年化标准差 = 收益率标准差 * np.sqrt(252)
    
    return (年化收益率均值 - 无风险利率) / 年化标准差


def 计算最大回撤(净值序列):
    """
    计算最大回撤
    最大回撤 = (峰值 - 谷值) / 峰值
    """
    if len(净值序列) < 2:
        return 0, 0
    
    净值数组 = np.array(净值序列)
    峰值 = np.maximum.accumulate(净值数组)
    回撤 = (峰值 - 净值数组) / 峰值
    最大回撤率 = np.max(回撤)
    最大回撤值 = np.max(峰值 - 净值数组)
    
    return 最大回撤值, 最大回撤率


def 计算胜率(交易记录):
    """
    计算胜率 = 盈利次数 / 总交易次数
    """
    if not 交易记录:
        return 0, 0, 0
    
    盈利交易 = [t for t in 交易记录 if t.get('盈亏', 0) > 0]
    亏损交易 = [t for t in 交易记录 if t.get('盈亏', 0) < 0]
    
    总次数 = len(交易记录)
    盈利次数 = len(盈利交易)
    亏损次数 = len(亏损交易)
    
    胜率 = 盈利次数 / 总次数 if 总次数 > 0 else 0
    
    return 胜率, 盈利次数, 亏损次数


def 计算盈亏比(交易记录):
    """
    计算盈亏比 = 平均盈利 / 平均亏损
    """
    盈利交易 = [t.get('盈亏', 0) for t in 交易记录 if t.get('盈亏', 0) > 0]
    亏损交易 = [abs(t.get('盈亏', 0)) for t in 交易记录 if t.get('盈亏', 0) < 0]
    
    平均盈利 = np.mean(盈利交易) if 盈利交易 else 0
    平均亏损 = np.mean(亏损交易) if 亏损交易 else 1
    
    return 平均盈利 / 平均亏损 if 平均亏损 > 0 else 0


def 计算换手率(交易记录, 初始资金):
    """
    计算换手率 = 总成交额 / 平均资金
    """
    if not 交易记录:
        return 0
    
    总成交额 = sum([t.get('价格', 0) * t.get('数量', 0) for t in 交易记录])
    平均资金 = 初始资金
    
    return 总成交额 / 平均资金


def 计算年化收益率(最终资金, 初始资金, 天数):
    """
    计算年化收益率
    年化收益率 = (最终资金/初始资金)^(252/天数) - 1
    """
    if 天数 <= 0 or 初始资金 <= 0:
        return 0
    
    总收益率 = (最终资金 - 初始资金) / 初始资金
    年化 = (1 + 总收益率) ** (252 / 天数) - 1
    
    return 年化


def 计算完整指标(每日净值列表, 交易记录, 初始资金, 最终资金):
    """
    计算所有评价指标
    """
    if not 每日净值列表:
        return {
            "总收益率": 0,
            "年化收益率": 0,
            "夏普比率": 0,
            "最大回撤": 0,
            "最大回撤率": 0,
            "胜率": 0,
            "盈亏比": 0,
            "交易次数": 0,
            "盈利次数": 0,
            "亏损次数": 0,
            "换手率": 0
        }
    
    # 提取净值序列
    净值序列 = [item['净值'] for item in 每日净值列表]
    
    # 计算收益率
    收益率序列 = []
    for i in range(1, len(净值序列)):
        if 净值序列[i-1] > 0:
            收益率序列.append((净值序列[i] - 净值序列[i-1]) / 净值序列[i-1])
    
    # 计算天数
    天数 = len(净值序列)
    
    # 计算各项指标
    最大回撤值, 最大回撤率 = 计算最大回撤(净值序列)
    夏普比率 = 计算夏普比率(收益率序列)
    年化收益率 = 计算年化收益率(最终资金, 初始资金, 天数)
    总收益率 = (最终资金 - 初始资金) / 初始资金 if 初始资金 > 0 else 0
    胜率, 盈利次数, 亏损次数 = 计算胜率(交易记录)
    盈亏比 = 计算盈亏比(交易记录)
    换手率 = 计算换手率(交易记录, 初始资金)
    
    return {
        "总收益率": round(总收益率, 4),
        "年化收益率": round(年化收益率, 4),
        "夏普比率": round(夏普比率, 2),
        "最大回撤": round(最大回撤值, 2),
        "最大回撤率": round(最大回撤率, 4),
        "胜率": round(胜率, 4),
        "盈亏比": round(盈亏比, 2),
        "交易次数": len(交易记录),
        "盈利次数": 盈利次数,
        "亏损次数": 亏损次数,
        "换手率": round(换手率, 2)
    }
