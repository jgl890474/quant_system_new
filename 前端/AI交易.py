def 获取运行中策略列表(策略加载器, 市场过滤=None):
    """从策略加载器获取运行中的策略列表"""
    if 策略加载器 is None:
        return []
    
    try:
        所有策略 = 策略加载器.获取策略()
        
        # 市场类别映射
        市场映射 = {
            "💰 外汇": "外汇",
            "₿ 加密货币": "加密货币",
            "📈 A股": "A股",
            "🇺🇸 美股": "美股",
            "📊 期货": "期货",
        }
        
        运行中策略 = []
        for 策略 in 所有策略:
            策略名称 = 策略.get("名称", "")
            策略类别 = 策略.get("类别", "")
            
            # 获取市场名称
            市场名称 = 市场映射.get(策略类别, "")
            if not 市场名称:
                if "外汇" in 策略类别:
                    市场名称 = "外汇"
                elif "加密" in 策略类别:
                    市场名称 = "加密货币"
                elif "A股" in 策略类别:
                    市场名称 = "A股"
                elif "美股" in 策略类别:
                    市场名称 = "美股"
                elif "期货" in 策略类别:
                    市场名称 = "期货"
            
            # ========== 关键：只显示运行中的策略 ==========
            if not 策略运行器.获取策略状态(策略名称):
                continue
            
            # 按市场过滤
            if 市场过滤 and 市场名称 != 市场过滤:
                continue
            
            运行中策略.append({
                "名称": 策略名称,
                "品种": 策略.get("品种", ""),
                "类别": 策略类别,
                "市场": 市场名称,
                "原始策略": 策略
            })
        
        return 运行中策略
    except Exception as e:
        print(f"获取策略列表失败: {e}")
        return []
