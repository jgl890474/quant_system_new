# -*- coding: utf-8 -*-
import os
import glob
import importlib.util

class 策略加载器:
    def __init__(self, 策略路径="策略库"):
        self.策略路径 = 策略路径
        self.策略列表 = []
        
        # 直接使用默认策略，不依赖文件系统
        self._使用默认策略()
    
    def _使用默认策略(self):
        """使用默认策略"""
        默认策略 = [
            {"名称": "外汇利差策略", "类别": "💰 外汇", "品种": "EURUSD"},
            {"名称": "加密双均线", "类别": "₿ 加密货币", "品种": "BTC-USD"},
            {"名称": "加密风控策略", "类别": "₿ 加密货币", "品种": "BTC-USD"},
            {"名称": "A股隔夜套利策略", "类别": "📈 A股", "品种": "000001.SS"},
            {"名称": "A股双均线", "类别": "📈 A股", "品种": "000001.SS"},
            {"名称": "A股量价策略", "类别": "📈 A股", "品种": "000001.SS"},
            {"名称": "美股简单策略", "类别": "🇺🇸 美股", "品种": "AAPL"},
            {"名称": "美股动量策略", "类别": "🇺🇸 美股", "品种": "AAPL"},
        ]
        
        for 策略 in 默认策略:
            self.策略列表.append({
                "名称": 策略["名称"],
                "类": None,
                "品种": 策略["品种"],
                "类别": 策略["类别"],
            })
        
        print(f"📊 已加载 {len(self.策略列表)} 个默认策略")
    
    def 获取策略(self):
        return self.策略列表
    
    def 获取策略列表(self):
        return self.策略列表
    
    def 获取策略列表_带状态(self):
        结果 = []
        for s in self.策略列表:
            结果.append({
                "名称": s.get("名称", ""),
                "类别": s.get("类别", ""),
                "品种": s.get("品种", ""),
                "启用": True,
            })
        return 结果
    
    def 根据名称获取(self, 名称):
        for s in self.策略列表:
            if s["名称"] == 名称:
                return s
        return None
    
    def 刷新(self):
        print("🔄 刷新策略列表")
        self.策略列表 = []
        self._使用默认策略()
