# -*- coding: utf-8 -*-

class 资金分配器:
    """
    资金分配器 - 将总资金分配给多个策略
    
    支持分配模式:
    - 平均分配: 每个策略获得相同资金
    - 按权重分配: 根据策略权重分配
    - 按风险分配: 低风险策略获得更多资金
    """
    
    def __init__(self, 总资金, 保留资金=0):
        """
        初始化资金分配器
        
        参数:
            总资金: 总可用资金
            保留资金: 保留不分配的资金（用于手续费、备用）
        """
        self.总资金 = 总资金
        self.保留资金 = 保留资金
        self.可分配资金 = 总资金 - 保留资金
        print(f"💰 资金分配器初始化")
        print(f"   总资金: ¥{总资金:,.2f}")
        print(f"   保留资金: ¥{保留资金:,.2f}")
        print(f"   可分配资金: ¥{self.可分配资金:,.2f}")
    
    def 平均分配(self, 策略列表):
        """
        将可分配资金平均分配给策略列表中的每个策略
        
        参数:
            策略列表: 策略对象列表（需要有资金属性）
        
        返回:
            分配结果字典
        """
        if not 策略列表:
            print("⚠️ 策略列表为空，无法分配资金")
            return {"成功": False, "错误": "策略列表为空"}
        
        策略数量 = len(策略列表)
        per = self.可分配资金 / 策略数量
        
        for s in 策略列表:
            if hasattr(s, '初始资金'):
                s.初始资金 = per
            if hasattr(s, '资金'):
                s.资金 = per
            # 记录分配金额
            s.分配资金 = per
        
        print(f"✅ 平均分配完成: {策略数量} 个策略")
        print(f"   每个策略: ¥{per:,.2f}")
        
        return {
            "成功": True,
            "分配模式": "平均分配",
            "策略数量": 策略数量,
            "每策略资金": per,
            "总分配": per * 策略数量,
            "保留资金": self.保留资金
        }
    
    def 按权重分配(self, 策略列表, 权重列表=None):
        """
        按权重分配资金
        
        参数:
            策略列表: 策略对象列表
            权重列表: 每个策略的权重，如 [0.3, 0.3, 0.4] 或 [30, 30, 40]
        
        返回:
            分配结果字典
        """
        if not 策略列表:
            return {"成功": False, "错误": "策略列表为空"}
        
        策略数量 = len(策略列表)
        
        # 如果没有提供权重，默认平均
        if 权重列表 is None:
            权重列表 = [1] * 策略数量
        
        # 确保权重列表长度匹配
        if len(权重列表) != 策略数量:
            print(f"⚠️ 权重列表长度({len(权重列表)})与策略数量({策略数量})不匹配，使用平均分配")
            return self.平均分配(策略列表)
        
        # 计算权重总和
        总权重 = sum(权重列表)
        
        # 分配资金
        分配结果 = []
        for i, s in enumerate(策略列表):
            权重 = 权重列表[i]
            资金 = self.可分配资金 * (权重 / 总权重)
            
            if hasattr(s, '初始资金'):
                s.初始资金 = 资金
            if hasattr(s, '资金'):
                s.资金 = 资金
            s.分配资金 = 资金
            s.分配权重 = 权重
            
            分配结果.append({
                "策略": getattr(s, '名称', f"策略{i+1}"),
                "权重": 权重,
                "资金": 资金
            })
        
        print(f"✅ 按权重分配完成: {策略数量} 个策略")
        for r in 分配结果:
            print(f"   {r['策略']}: 权重{r['权重']} -> ¥{r['资金']:,.2f}")
        
        return {
            "成功": True,
            "分配模式": "按权重分配",
            "策略数量": 策略数量,
            "分配详情": 分配结果,
            "总分配": sum(r["资金"] for r in 分配结果),
            "保留资金": self.保留资金
        }
    
    def 按风险分配(self, 策略列表, 风险等级列表=None):
        """
        按风险分配资金 - 低风险多分配，高风险少分配
        
        参数:
            策略列表: 策略对象列表
            风险等级列表: 每个策略的风险等级 1-10 (1最低风险，10最高风险)
        
        返回:
            分配结果字典
        """
        if not 策略列表:
            return {"成功": False, "错误": "策略列表为空"}
        
        策略数量 = len(策略列表)
        
        # 如果没有提供风险等级，默认中等风险
        if 风险等级列表 is None:
            风险等级列表 = [5] * 策略数量
        
        # 计算权重 = 1/风险等级（风险越低权重越高）
        权重列表 = [1 / max(r, 1) for r in 风险等级列表]
        
        return self.按权重分配(策略列表, 权重列表)
    
    def 获取分配详情(self, 策略列表, 分配模式="平均"):
        """
        获取分配详情（不实际分配，仅预览）
        
        参数:
            策略列表: 策略对象列表
            分配模式: "平均" / "权重" / "风险"
        
        返回:
            分配详情列表
        """
        if not 策略列表:
            return []
        
        策略数量 = len(策略列表)
        per = self.可分配资金 / 策略数量
        
        if 分配模式 == "平均":
            return [{
                "策略": getattr(s, '名称', f"策略{i+1}"),
                "资金": per,
                "占比": 100 / 策略数量
            } for i, s in enumerate(策略列表)]
        
        elif 分配模式 == "权重":
            return [{
                "策略": getattr(s, '名称', f"策略{i+1}"),
                "资金": per,
                "占比": 100 / 策略数量,
                "提示": "请提供权重列表"
            } for i, s in enumerate(策略列表)]
        
        else:
            return [{
                "策略": getattr(s, '名称', f"策略{i+1}"),
                "资金": per,
                "占比": 100 / 策略数量,
                "提示": "请提供风险等级列表"
            } for i, s in enumerate(策略列表)]
    
    def 设置策略资金(self, 策略, 资金):
        """
        为单个策略设置资金
        
        参数:
            策略: 策略对象
            资金: 分配的资金
        
        返回:
            是否成功
        """
        if 资金 > self.可分配资金:
            print(f"⚠️ 资金不足: 需要 ¥{资金:,.2f}, 可用 ¥{self.可分配资金:,.2f}")
            return False
        
        if hasattr(策略, '初始资金'):
           策略.初始资金 = 资金
        if hasattr(策略, '资金'):
           策略.资金 = 资金
        策略.分配资金 = 资金
        
        print(f"✅ 策略 {getattr(策略, '名称', '未知')} 分配 ¥{资金:,.2f}")
        return True
    
    def 查看分配状态(self, 策略列表):
        """查看当前分配状态"""
        print("\n" + "="*50)
        print("📊 资金分配状态")
        print("="*50)
        print(f"总资金: ¥{self.总资金:,.2f}")
        print(f"保留资金: ¥{self.保留资金:,.2f}")
        print(f"已分配资金: ¥{self.可分配资金 - sum(getattr(s, '资金', 0) for s in 策略列表):,.2f}")
        print("-"*50)
        
        for s in 策略列表:
            名称 = getattr(s, '名称', '未知')
            资金 = getattr(s, '资金', 0)
            占比 = (资金 / self.总资金 * 100) if self.总资金 > 0 else 0
            print(f"{名称}: ¥{资金:,.2f} ({占比:.1f}%)")
        
        print("="*50)
        
        return {
            "总资金": self.总资金,
            "保留资金": self.保留资金,
            "策略分配": [{"名称": getattr(s, '名称', '未知'), "资金": getattr(s, '资金', 0)} for s in 策略列表]
        }


# 便捷函数
def 创建资金分配器(总资金, 保留资金=0):
    """创建资金分配器实例"""
    return 资金分配器(总资金, 保留资金)


def 快速分配(策略列表, 总资金, 模式="平均"):
    """
    快速分配资金
    
    参数:
        策略列表: 策略对象列表
        总资金: 总资金
        模式: "平均" / "权重" / "风险"
    
    返回:
        分配器实例和分配结果
    """
    分配器 = 资金分配器(总资金)
    
    if 模式 == "平均":
        结果 = 分配器.平均分配(策略列表)
    elif 模式 == "权重":
        结果 = 分配器.按权重分配(策略列表)
    elif 模式 == "风险":
        结果 = 分配器.按风险分配(策略列表)
    else:
        结果 = 分配器.平均分配(策略列表)
    
    return 分配器, 结果
