def 买入(self, 品种, 价格, 数量=1000):
    """执行买入"""
    try:
        from .数据模型 import 持仓数据
    except:
        class 持仓数据:
            def __init__(self, 品种, 数量, 平均成本):
                self.品种 = 品种
                self.数量 = 数量
                self.平均成本 = 平均成本
                self.当前价格 = 平均成本
                self.已实现盈亏 = 0
    
    # 计算需要资金
    需要资金 = 价格 * 数量
    
    # 检查可用资金
    可用资金 = self.获取可用资金()
    if 需要资金 > 可用资金:
        st.session_state['错误消息'] = f"❌ 资金不足！需要 ¥{需要资金:,.0f}，可用 ¥{可用资金:,.0f}"
        st.rerun()
        return
    
    # 执行买入 - 重要：扣减资金通过已实现盈亏实现
    if 品种 in self.持仓:
        pos = self.持仓[品种]
        总数量 = pos.数量 + 数量
        总成本 = pos.数量 * pos.平均成本 + 数量 * 价格
        pos.数量 = 总数量
        pos.平均成本 = 总成本 / 总数量
        pos.当前价格 = 价格
    else:
        new_pos = 持仓数据(品种, 数量, 价格)
        new_pos.当前价格 = 价格
        self.持仓[品种] = new_pos
    
    # 关键：买入时已实现盈亏减少（相当于扣钱）
    self.已实现盈亏 -= 需要资金
    
    self.交易记录.append({
        "时间": 获取当前时间(), 
        "动作": "买入", 
        "品种": 品种, 
        "价格": 价格, 
        "数量": 数量
    })
    
    try:
        数据库.保存交易记录("买入", 品种, 价格, 数量)
    except:
        pass
    self._保存持仓到数据库()
    
    st.session_state['成功消息'] = f"✅ 买入 {品种} {数量}股 @ {价格:.4f}"
    st.rerun()
