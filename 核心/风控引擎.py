    def 检查持仓风控(self, 引擎):
        """
        检查所有持仓是否触发止损/止盈
        返回: 需要平仓的品种列表
        """
        需要平仓 = []
        总资金 = 引擎.获取总资产()
        
        for 品种, 持仓 in 引擎.持仓.items():
            盈亏比例 = (持仓.当前价格 - 持仓.平均成本) / 持仓.平均成本
            
            # 止损检查
            if 盈亏比例 <= -self.止损比例:
                需要平仓.append({
                    "品种": 品种,
                    "原因": "止损",
                    "盈亏比例": f"{盈亏比例*100:.2f}%",
                    "盈亏": (持仓.当前价格 - 持仓.平均成本) * 持仓.数量
                })
            
            # 止盈检查
            elif 盈亏比例 >= self.止盈比例:
                需要平仓.append({
                    "品种": 品种,
                    "原因": "止盈",
                    "盈亏比例": f"{盈亏比例*100:.2f}%",
                    "盈亏": (持仓.当前价格 - 持仓.平均成本) * 持仓.数量
                })
        
        return 需要平仓
    
    def 执行风控平仓(self, 引擎):
        """自动执行风控平仓"""
        需要平仓 = self.检查持仓风控(引擎)
        平仓记录 = []
        
        for 风控项 in 需要平仓:
            品种 = 风控项["品种"]
            持仓 = 引擎.持仓.get(品种)
            if 持仓:
                价格 = 持仓.当前价格
                引擎.卖出(品种, 价格, 持仓.数量)
                平仓记录.append({
                    "时间": datetime.now(),
                    "品种": 品种,
                    "原因": 风控项["原因"],
                    "盈亏": 风控项["盈亏"]
                })
        
        return 平仓记录
    
    def 更新每日盈亏(self, 盈亏):
        """更新今日盈亏统计"""
        today = date.today()
        if today != self.今日交易日期:
            # 新的一天，重置统计
            self.今日交易日期 = today
            self.今日盈亏 = 0
            self.今日交易次数 = 0
        
        self.今日盈亏 += 盈亏
        self.今日交易次数 += 1
    
    def 获取风控状态(self, 引擎, 总资金):
        """获取当前风控状态摘要"""
        当前总仓位 = sum(pos.数量 * pos.当前价格 for pos in 引擎.持仓.values())
        
        return {
            "总仓位比例": f"{当前总仓位/总资金*100:.1f}%",
            "最大总仓位限制": f"{self.最大总仓位比例*100}%",
            "单品种仓位限制": f"{self.最大单品种仓位比例*100}%",
            "止损线": f"-{self.止损比例*100}%",
            "止盈线": f"+{self.止盈比例*100}%",
            "今日盈亏": self.今日盈亏,
            "今日交易次数": self.今日交易次数,
            "每日亏损上限": f"-{self.每日最大亏损*100}%"
        }
