# -*- coding: utf-8 -*-
import streamlit as st
from datetime import datetime, date
import logging
from 核心 import 行情获取

# 配置日志
logger = logging.getLogger(__name__)


class 风控引擎:
    """风控引擎 - 止损、止盈、移动止损、仓位管理"""
    
    def __init__(self, 配置=None):
        self.配置 = 配置 or {}
        
        # 风控参数
        self.止损比例 = self.配置.get("止损比例", 0.02)      # 2%
        self.止盈比例 = self.配置.get("止盈比例", 0.04)      # 4%
        self.移动止损开关 = self.配置.get("移动止损开关", True)
        self.移动止损回撤 = self.配置.get("移动止损回撤", 0.02)  # 2%回撤触发
        self.最大单品种仓位比例 = self.配置.get("最大单品种仓位比例", 0.3)
        self.最大总仓位比例 = self.配置.get("最大总仓位比例", 0.8)
        self.每日最大亏损 = self.配置.get("每日最大亏损", 0.05)
        
        # 移动止损记录 {品种: {"最高价": xx, "止损价": xx, "方向": "long"}}
        self.移动止损记录 = {}
        
        # 每日统计
        self.今日交易日期 = date.today()
        self.今日盈亏 = 0
        self.今日交易次数 = 0
        
        # 策略资金池
        self.策略资金池 = {}
        
        print(f"✅ 风控引擎初始化完成")
        print(f"   止损: {self.止损比例*100}% | 止盈: {self.止盈比例*100}%")
        print(f"   移动止损: {'开启' if self.移动止损开关 else '关闭'} | 最大回撤: {self.移动止损回撤*100}%")
        print(f"   单品种仓位: {self.最大单品种仓位比例*100}% | 总仓位: {self.最大总仓位比例*100}%")
    
    def 设置止损止盈(self, 品种, 入场价, 方向="long"):
        """为持仓设置止损止盈价位"""
        if 方向 == "long":
            止损价 = 入场价 * (1 - self.止损比例)
            止盈价 = 入场价 * (1 + self.止盈比例)
            self.移动止损记录[品种] = {
                "最高价": 入场价, 
                "止损价": 止损价,
                "方向": "long"
            }
        else:
            止损价 = 入场价 * (1 + self.止损比例)
            止盈价 = 入场价 * (1 - self.止盈比例)
            self.移动止损记录[品种] = {
                "最低价": 入场价, 
                "止损价": 止损价,
                "方向": "short"
            }
        
        print(f"📊 设置风控: {品种} 止损={止损价:.4f} 止盈={止盈价:.4f}")
        
        return {"止损价": round(止损价, 4), "止盈价": round(止盈价, 4)}
    
    def 检查移动止损(self, 品种, 当前价, 方向="long"):
        """检查移动止损"""
        if 品种 not in self.移动止损记录:
            return None
        
        record = self.移动止损记录[品种]
        
        if 方向 == "long":
            # 多头：更新最高价
            if 当前价 > record.get("最高价", 当前价):
                record["最高价"] = 当前价
                # 移动止损价 = 最高价 * (1 - 移动止损回撤)
                new_stop = record["最高价"] * (1 - self.移动止损回撤)
                if new_stop > record.get("止损价", new_stop):
                    record["止损价"] = new_stop
                    print(f"📈 移动止损: {品种} 最高价={record['最高价']:.4f} 止损上调至={record['止损价']:.4f}")
                    return {"触发价": record["止损价"], "类型": "移动止损"}
        else:
            # 空头：更新最低价
            if 当前价 < record.get("最低价", 当前价):
                record["最低价"] = 当前价
                # 移动止损价 = 最低价 * (1 + 移动止损回撤)
                new_stop = record["最低价"] * (1 + self.移动止损回撤)
                if new_stop < record.get("止损价", new_stop):
                    record["止损价"] = new_stop
                    print(f"📉 移动止损: {品种} 最低价={record['最低价']:.4f} 止损下调至={record['止损价']:.4f}")
                    return {"触发价": record["止损价"], "类型": "移动止损"}
        
        return None
    
    def 检查止损止盈(self, 品种, 入场价, 当前价, 方向="long"):
        """
        检查是否触发止损止盈
        重要：这只检查止损止盈，不干预AI的正常卖出信号
        """
        if 方向 == "long":
            盈亏率 = (当前价 - 入场价) / 入场价
        else:
            盈亏率 = (入场价 - 当前价) / 入场价
        
        # 检查移动止损
        if self.移动止损开关:
            移动结果 = self.检查移动止损(品种, 当前价, 方向)
            if 移动结果 and ((方向 == "long" and 当前价 <= 移动结果["触发价"]) or
                            (方向 == "short" and 当前价 >= 移动结果["触发价"])):
                return {"触发": True, "类型": "移动止损", "盈亏率": 盈亏率}
        
        # 检查固定止损（保护本金）
        if 盈亏率 <= -self.止损比例:
            return {"触发": True, "类型": "止损", "盈亏率": 盈亏率}
        
        # 检查固定止盈（锁定利润）
        if 盈亏率 >= self.止盈比例:
            return {"触发": True, "类型": "止盈", "盈亏率": 盈亏率}
        
        return {"触发": False, "类型": None, "盈亏率": 盈亏率}
    
    def 监控持仓(self, 引擎):
        """
        实时监控所有持仓，自动执行止损止盈
        注意：只返回需要风控平仓的列表，不干预AI信号
        """
        触发列表 = []
        
        if not 引擎 or not hasattr(引擎, '持仓'):
            return 触发列表
        
        for 品种, pos in 引擎.持仓.items():
            try:
                # 获取当前价格
                价格结果 = 行情获取.获取价格(品种)
                if 价格结果 and hasattr(价格结果, '价格'):
                    当前价 = 价格结果.价格
                else:
                    当前价 = getattr(pos, '当前价格', getattr(pos, '平均成本', 0))
                
                if 当前价 <= 0:
                    continue
                
                pos.当前价格 = 当前价
                
                # 只检查止损止盈（风控保护）
                结果 = self.检查止损止盈(品种, pos.平均成本, 当前价, "long")
                
                if 结果["触发"]:
                    触发列表.append({
                        "品种": 品种,
                        "类型": 结果["类型"],
                        "盈亏率": 结果["盈亏率"],
                        "当前价": 当前价,
                        "数量": pos.数量
                    })
                    print(f"⚠️ 触发风控: {品种} - {结果['类型']} (盈亏率: {结果['盈亏率']*100:.2f}%)")
                    
            except Exception as e:
                print(f"监控 {品种} 时出错: {e}")
                continue
        
        return 触发列表
    
    def 执行自动平仓(self, 引擎):
        """执行自动止损止盈平仓（只执行风控平仓，不干预AI信号）"""
        触发列表 = self.监控持仓(引擎)
        平仓记录 = []
        
        for 触发 in 触发列表:
            品种 = 触发["品种"]
            
            # 检查是否已经被平仓
            if 品种 not in 引擎.持仓:
                continue
            
            当前价 = 触发["当前价"]
            数量 = 触发["数量"]
            
            # 执行卖出（使用风控策略名称）
            结果 = 引擎.卖出(品种, 当前价, 数量, 策略名称="风控平仓")
            
            if 结果.get("success"):
                盈亏金额 = (当前价 - 引擎.持仓.get(品种, {}).平均成本) * 数量 if 品种 in 引擎.持仓 else 0
                平仓记录.append({
                    "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
                    "品种": 品种,
                    "类型": 触发["类型"],
                    "价格": 当前价,
                    "盈亏率": f"{触发['盈亏率']*100:.2f}%",
                    "盈亏金额": round(盈亏金额, 2),
                    "数量": 数量
                })
                
                # 更新每日盈亏
                self.更新每日盈亏(盈亏金额)
                
                # 清除移动止损记录
                if 品种 in self.移动止损记录:
                    del self.移动止损记录[品种]
                
                print(f"✅ 自动平仓: {品种} ({触发['类型']}) @ {当前价:.4f}")
        
        return 平仓记录
    
    def 检查新交易(self, 品种, 方向, 数量, 价格, 引擎, 总资金):
        """检查新交易是否允许执行"""
        # 每日亏损限制
        if self.今日盈亏 <= -总资金 * self.每日最大亏损:
            return False, f"今日已触发每日亏损限制 ({self.今日盈亏:.2f})"
        
        # 单品种仓位限制
        已有持仓 = 引擎.持仓.get(品种)
        if 已有持仓:
            当前持仓金额 = 已有持仓.数量 * 价格
            新总持仓金额 = 当前持仓金额 + 数量 * 价格
        else:
            新总持仓金额 = 数量 * 价格
        
        if 新总持仓金额 > 总资金 * self.最大单品种仓位比例:
            return False, f"单品种仓位超限: {品种} (限制: {self.最大单品种仓位比例*100}%)"
        
        # 总仓位限制
        当前总仓位 = 0
        for s, pos in 引擎.持仓.items():
            try:
                价格结果 = 行情获取.获取价格(s)
                if 价格结果 and hasattr(价格结果, '价格'):
                    当前价 = 价格结果.价格
                else:
                    当前价 = getattr(pos, '当前价格', pos.平均成本)
                当前总仓位 += pos.数量 * 当前价
            except:
                当前总仓位 += pos.数量 * pos.平均成本
        
        新总仓位 = 当前总仓位 + 数量 * 价格
        
        if 新总仓位 > 总资金 * self.最大总仓位比例:
            return False, f"总仓位超限 (限制: {self.最大总仓位比例*100}%)"
        
        return True, "通过"
    
    def 检查持仓风控(self, 引擎):
        """检查所有持仓是否触发止损/止盈"""
        return self.监控持仓(引擎)
    
    def 执行风控平仓(self, 引擎):
        """自动执行风控平仓"""
        return self.执行自动平仓(引擎)
    
    def 更新每日盈亏(self, 盈亏):
        """更新今日盈亏统计"""
        today = date.today()
        if today != self.今日交易日期:
            # 新的一天，重置统计
            self.今日交易日期 = today
            self.今日盈亏 = 0
            self.今日交易次数 = 0
            print("📅 新的一天，重置每日盈亏统计")
        
        self.今日盈亏 += 盈亏
        self.今日交易次数 += 1
    
    def 获取风控状态(self, 引擎, 总资金):
        """获取当前风控状态摘要"""
        当前总仓位 = 0
        for s, pos in 引擎.持仓.items():
            try:
                价格结果 = 行情获取.获取价格(s)
                if 价格结果 and hasattr(价格结果, '价格'):
                    当前价 = 价格结果.价格
                else:
                    当前价 = getattr(pos, '当前价格', pos.平均成本)
                当前总仓位 += pos.数量 * 当前价
            except:
                当前总仓位 += pos.数量 * pos.平均成本
        
        剩余亏损空间 = (-总资金 * self.每日最大亏损) - self.今日盈亏
        
        return {
            "总仓位比例": f"{当前总仓位/总资金*100:.1f}%",
            "最大总仓位限制": f"{self.最大总仓位比例*100}%",
            "单品种仓位限制": f"{self.最大单品种仓位比例*100}%",
            "止损线": f"-{self.止损比例*100}%",
            "止盈线": f"+{self.止盈比例*100}%",
            "移动止损": "开启" if self.移动止损开关 else "关闭",
            "移动止损回撤": f"{self.移动止损回撤*100}%",
            "今日盈亏": f"¥{self.今日盈亏:+,.2f}",
            "今日交易次数": self.今日交易次数,
            "每日亏损上限": f"-{self.每日最大亏损*100}%",
            "剩余亏损空间": f"¥{max(0,剩余亏损空间):,.2f}"
        }
    
    def 重置今日统计(self):
        """手动重置今日统计数据"""
        self.今日交易日期 = date.today()
        self.今日盈亏 = 0
        self.今日交易次数 = 0
        print("✅ 已重置今日统计")
        return {"success": True}
    
    def 清除移动止损记录(self, 品种=None):
        """清除移动止损记录"""
        if 品种:
            if 品种 in self.移动止损记录:
                del self.移动止损记录[品种]
                print(f"🗑️ 已清除 {品种} 的移动止损记录")
        else:
            self.移动止损记录 = {}
            print("🗑️ 已清除所有移动止损记录")
    
    def 获取风控参数(self):
        """获取当前风控参数"""
        return {
            "止损比例": self.止损比例,
            "止盈比例": self.止盈比例,
            "移动止损开关": self.移动止损开关,
            "移动止损回撤": self.移动止损回撤,
            "最大单品种仓位比例": self.最大单品种仓位比例,
            "最大总仓位比例": self.最大总仓位比例,
            "每日最大亏损": self.每日最大亏损
        }
    
    # ========== 策略资金分配和全局资金控制 ==========
    
    def 初始化策略资金(self, 总资金, 策略配置列表):
        """为每个策略分配独立资金池"""
        if not hasattr(self, '策略资金池'):
            self.策略资金池 = {}
        
        剩余资金 = 总资金
        
        for 配置 in 策略配置列表:
            if not 配置.get("启用", False):
                continue
            
            策略名 = 配置.get("名称", "未知")
            资金比例 = 配置.get("资金分配", 0.1)
            分配资金 = 总资金 * 资金比例
            
            self.策略资金池[策略名] = {
                "总额": round(分配资金, 2),
                "已用": 0,
                "可用": round(分配资金, 2),
                "比例": 资金比例
            }
            剩余资金 -= 分配资金
            
            print(f"   📊 策略 [{策略名}] 分配资金: ¥{分配资金:,.0f} ({资金比例*100:.0f}%)")
        
        if 剩余资金 > 0:
            self.策略资金池["备用池"] = {
                "总额": round(剩余资金, 2),
                "已用": 0,
                "可用": round(剩余资金, 2),
                "比例": 剩余资金 / 总资金
            }
            print(f"   📊 备用池 分配资金: ¥{剩余资金:,.0f}")
        
        return self.策略资金池
    
    def 检查策略资金(self, 策略名, 价格=0):
        """检查策略是否有足够资金买入"""
        if not hasattr(self, '策略资金池'):
            return {"允许": True, "可用资金": 0, "建议数量": 0, "原因": ""}
        
        if 策略名 not in self.策略资金池:
            return {"允许": True, "可用资金": 0, "建议数量": 0, "原因": ""}
        
        策略资金 = self.策略资金池[策略名]
        
        if 策略资金["可用"] <= 0:
            return {
                "允许": False,
                "可用资金": 0,
                "建议数量": 0,
                "原因": f"策略 [{策略名}] 资金已用完"
            }
        
        建议数量 = 策略资金["可用"] / 价格 if 价格 > 0 else 0
        
        return {
            "允许": True,
            "可用资金": 策略资金["可用"],
            "建议数量": round(建议数量, 4),
            "原因": ""
        }
    
    def 扣除策略资金(self, 策略名, 金额):
        """扣除策略资金（买入后调用）"""
        if not hasattr(self, '策略资金池'):
            return
        
        if 策略名 in self.策略资金池:
            self.策略资金池[策略名]["已用"] += 金额
            self.策略资金池[策略名]["可用"] -= 金额
            print(f"💰 策略 [{策略名}] 扣除资金: ¥{金额:.2f}, 剩余可用: ¥{self.策略资金池[策略名]['可用']:.2f}")
    
    def 释放策略资金(self, 策略名, 金额):
        """释放策略资金（卖出后调用）"""
        if not hasattr(self, '策略资金池'):
            return
        
        if 策略名 in self.策略资金池:
            self.策略资金池[策略名]["已用"] -= 金额
            self.策略资金池[策略名]["可用"] += 金额
            print(f"💰 策略 [{策略名}] 释放资金: ¥{金额:.2f}, 可用: ¥{self.策略资金池[策略名]['可用']:.2f}")
    
    def 获取策略资金状态(self):
        """获取策略资金分配状态"""
        if not hasattr(self, '策略资金池'):
            return {}
        
        return self.策略资金池
    
    def 检查全局仓位限制(self, 引擎, 新增金额=0):
        """检查全局仓位限制"""
        总资金 = getattr(引擎, '初始资金', 1000000)
        
        当前总仓位 = 0
        for 品种, pos in 引擎.持仓.items():
            成本 = getattr(pos, '平均成本', 0)
            数量 = getattr(pos, '数量', 0)
            当前总仓位 += 成本 * 数量
        
        新总仓位 = 当前总仓位 + 新增金额
        新仓位比例 = 新总仓位 / 总资金 if 总资金 > 0 else 0
        
        if 新仓位比例 >= self.最大总仓位比例:
            return {
                "允许": False,
                "当前仓位": round(新仓位比例 * 100, 1),
                "原因": f"总仓位 {新仓位比例*100:.1f}% 已达上限 {self.最大总仓位比例*100:.0f}%"
            }
        
        return {
            "允许": True,
            "当前仓位": round(新仓位比例 * 100, 1),
            "原因": ""
        }
    
    def 检查单品种仓位限制(self, 品种, 引擎, 新增数量=0, 价格=0):
        """检查单品种仓位限制"""
        总资金 = getattr(引擎, '初始资金', 1000000)
        
        if 品种 in 引擎.持仓:
            pos = 引擎.持仓[品种]
            成本 = getattr(pos, '平均成本', 0)
            数量 = getattr(pos, '数量', 0)
            当前金额 = 成本 * 数量
        else:
            当前金额 = 0
        
        新增金额 = 新增数量 * 价格 if 价格 > 0 else 0
        新总金额 = 当前金额 + 新增金额
        新占比 = 新总金额 / 总资金 if 总资金 > 0 else 0
        
        if 新占比 >= self.最大单品种仓位比例:
            return {
                "允许": False,
                "当前占比": round(新占比 * 100, 1),
                "原因": f"{品种} 仓位 {新占比*100:.1f}% 已达上限 {self.最大单品种仓位比例*100:.0f}%"
            }
        
        return {
            "允许": True,
            "当前占比": round(新占比 * 100, 1),
            "原因": ""
        }
    
    def 检查每日亏损限制(self, 引擎):
        """检查每日亏损限制"""
        总资金 = getattr(引擎, '初始资金', 1000000)
        
        if self.今日盈亏 <= -总资金 * self.每日最大亏损:
            return {
                "允许": False,
                "今日盈亏": self.今日盈亏,
                "原因": f"今日亏损 {self.今日盈亏:,.2f} 已达上限 {self.每日最大亏损*100:.0f}%"
            }
        
        return {"允许": True, "今日盈亏": self.今日盈亏, "原因": ""}
    
    def 检查完整风控(self, 品种, 策略名, 引擎, 新增数量, 价格):
        """完整风控检查（一次性检查所有限制）"""
        检查结果 = {}
        
        # 1. 检查策略资金
        策略资金检查 = self.检查策略资金(策略名, 价格)
        检查结果["策略资金"] = 策略资金检查
        if not 策略资金检查["允许"]:
            return {"允许": False, "原因": 策略资金检查["原因"], "检查结果": 检查结果}
        
        # 2. 检查每日亏损
        每日亏损检查 = self.检查每日亏损限制(引擎)
        检查结果["每日亏损"] = 每日亏损检查
        if not 每日亏损检查["允许"]:
            return {"允许": False, "原因": 每日亏损检查["原因"], "检查结果": 检查结果}
        
        # 3. 检查单品种仓位
        单品种检查 = self.检查单品种仓位限制(品种, 引擎, 新增数量, 价格)
        检查结果["单品种"] = 单品种检查
        if not 单品种检查["允许"]:
            return {"允许": False, "原因": 单品种检查["原因"], "检查结果": 检查结果}
        
        # 4. 检查全局仓位
        新增金额 = 新增数量 * 价格
        全局仓位检查 = self.检查全局仓位限制(引擎, 新增金额)
        检查结果["全局仓位"] = 全局仓位检查
        if not 全局仓位检查["允许"]:
            return {"允许": False, "原因": 全局仓位检查["原因"], "检查结果": 检查结果}
        
        return {"允许": True, "原因": "风控检查通过", "检查结果": 检查结果}


