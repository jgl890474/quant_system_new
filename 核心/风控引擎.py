    # ========== 以下为新增：策略资金分配和全局资金控制 ==========
    
    def 初始化策略资金(self, 总资金, 策略配置列表):
        """
        为每个策略分配独立资金池
        
        参数:
            总资金: 账户总资金
            策略配置列表: 策略配置列表（来自策略调度配置.json）
        """
        # 初始化策略资金池
        if not hasattr(self, '策略资金池'):
            self.策略资金池 = {}
        
        剩余资金 = 总资金
        
        for 配置 in 策略配置列表:
            if not 配置.get("启用", False):
                continue
            
            策略名 = 配置.get("名称", "未知")
            资金比例 = 配置.get("资金分配", 0.1)
            分配资金 = 总资金 * 资金比例
            
            self.策略资金池[策略名] = {
                "总额": round(分配资金, 2),
                "已用": 0,
                "可用": round(分配资金, 2),
                "比例": 资金比例
            }
            剩余资金 -= 分配资金
            
            print(f"   📊 策略 [{策略名}] 分配资金: ¥{分配资金:,.0f} ({资金比例*100:.0f}%)")
        
        # 剩余资金放入备用池
        if 剩余资金 > 0:
            self.策略资金池["备用池"] = {
                "总额": round(剩余资金, 2),
                "已用": 0,
                "可用": round(剩余资金, 2),
                "比例": 剩余资金 / 总资金
            }
            print(f"   📊 备用池 分配资金: ¥{剩余资金:,.0f}")
        
        return self.策略资金池
    
    def 检查策略资金(self, 策略名, 价格=0):
        """
        检查策略是否有足够资金买入
        
        参数:
            策略名: 策略名称
            价格: 当前价格（用于计算建议数量）
        
        返回:
            {"允许": bool, "可用资金": float, "建议数量": float, "原因": str}
        """
        if not hasattr(self, '策略资金池'):
            return {"允许": True, "可用资金": 0, "建议数量": 0, "原因": ""}
        
        if 策略名 not in self.策略资金池:
            return {"允许": True, "可用资金": 0, "建议数量": 0, "原因": ""}
        
        策略资金 = self.策略资金池[策略名]
        
        if 策略资金["可用"] <= 0:
            return {
                "允许": False,
                "可用资金": 0,
                "建议数量": 0,
                "原因": f"策略 [{策略名}] 资金已用完"
            }
        
        # 计算建议数量
        建议数量 = 策略资金["可用"] / 价格 if 价格 > 0 else 0
        
        return {
            "允许": True,
            "可用资金": 策略资金["可用"],
            "建议数量": round(建议数量, 4),
            "原因": ""
        }
    
    def 扣除策略资金(self, 策略名, 金额):
        """扣除策略资金（买入后调用）"""
        if not hasattr(self, '策略资金池'):
            return
        
        if 策略名 in self.策略资金池:
            self.策略资金池[策略名]["已用"] += 金额
            self.策略资金池[策略名]["可用"] -= 金额
            print(f"💰 策略 [{策略名}] 扣除资金: ¥{金额:.2f}, 剩余可用: ¥{self.策略资金池[策略名]['可用']:.2f}")
    
    def 释放策略资金(self, 策略名, 金额):
        """释放策略资金（卖出后调用）"""
        if not hasattr(self, '策略资金池'):
            return
        
        if 策略名 in self.策略资金池:
            self.策略资金池[策略名]["已用"] -= 金额
            self.策略资金池[策略名]["可用"] += 金额
            print(f"💰 策略 [{策略名}] 释放资金: ¥{金额:.2f}, 可用: ¥{self.策略资金池[策略名]['可用']:.2f}")
    
    def 获取策略资金状态(self):
        """获取策略资金分配状态"""
        if not hasattr(self, '策略资金池'):
            return {}
        
        return self.策略资金池
    
    def 检查全局仓位限制(self, 引擎, 新增金额=0):
        """
        检查全局仓位限制
        
        参数:
            引擎: 订单引擎实例
            新增金额: 本次新增金额
        
        返回:
            {"允许": bool, "当前仓位": float, "原因": str}
        """
        总资金 = getattr(引擎, '初始资金', 1000000)
        
        # 计算当前总仓位
        当前总仓位 = 0
        for 品种, pos in 引擎.持仓.items():
            成本 = getattr(pos, '平均成本', 0)
            数量 = getattr(pos, '数量', 0)
            当前总仓位 += 成本 * 数量
        
        新总仓位 = 当前总仓位 + 新增金额
        新仓位比例 = 新总仓位 / 总资金 if 总资金 > 0 else 0
        
        if 新仓位比例 >= self.最大总仓位比例:
            return {
                "允许": False,
                "当前仓位": round(新仓位比例 * 100, 1),
                "原因": f"总仓位 {新仓位比例*100:.1f}% 已达上限 {self.最大总仓位比例*100:.0f}%"
            }
        
        return {
            "允许": True,
            "当前仓位": round(新仓位比例 * 100, 1),
            "原因": ""
        }
    
    def 检查单品种仓位限制(self, 品种, 引擎, 新增数量=0, 价格=0):
        """
        检查单品种仓位限制
        
        参数:
            品种: 品种代码
            引擎: 订单引擎实例
            新增数量: 本次新增数量
            价格: 当前价格
        
        返回:
            {"允许": bool, "当前占比": float, "原因": str}
        """
        总资金 = getattr(引擎, '初始资金', 1000000)
        
        # 计算当前持仓金额
        if 品种 in 引擎.持仓:
            pos = 引擎.持仓[品种]
            成本 = getattr(pos, '平均成本', 0)
            数量 = getattr(pos, '数量', 0)
            当前金额 = 成本 * 数量
        else:
            当前金额 = 0
        
        新增金额 = 新增数量 * 价格 if 价格 > 0 else 0
        新总金额 = 当前金额 + 新增金额
        新占比 = 新总金额 / 总资金 if 总资金 > 0 else 0
        
        if 新占比 >= self.最大单品种仓位比例:
            return {
                "允许": False,
                "当前占比": round(新占比 * 100, 1),
                "原因": f"{品种} 仓位 {新占比*100:.1f}% 已达上限 {self.最大单品种仓位比例*100:.0f}%"
            }
        
        return {
            "允许": True,
            "当前占比": round(新占比 * 100, 1),
            "原因": ""
        }
    
    def 检查每日亏损限制(self, 引擎):
        """
        检查每日亏损限制
        
        返回:
            {"允许": bool, "今日盈亏": float, "原因": str}
        """
        总资金 = getattr(引擎, '初始资金', 1000000)
        
        if self.今日盈亏 <= -总资金 * self.每日最大亏损:
            return {
                "允许": False,
                "今日盈亏": self.今日盈亏,
                "原因": f"今日亏损 {self.今日盈亏:,.2f} 已达上限 {self.每日最大亏损*100:.0f}%"
            }
        
        return {"允许": True, "今日盈亏": self.今日盈亏, "原因": ""}
    
    def 检查完整风控(self, 品种, 策略名, 引擎, 新增数量, 价格):
        """
        完整风控检查（一次性检查所有限制）
        
        返回:
            {"允许": bool, "原因": str, "检查结果": dict}
        """
        检查结果 = {}
        
        # 1. 检查策略资金
        策略资金检查 = self.检查策略资金(策略名, 价格)
        检查结果["策略资金"] = 策略资金检查
        if not 策略资金检查["允许"]:
            return {"允许": False, "原因": 策略资金检查["原因"], "检查结果": 检查结果}
        
        # 2. 检查每日亏损
        每日亏损检查 = self.检查每日亏损限制(引擎)
        检查结果["每日亏损"] = 每日亏损检查
        if not 每日亏损检查["允许"]:
            return {"允许": False, "原因": 每日亏损检查["原因"], "检查结果": 检查结果}
        
        # 3. 检查单品种仓位
        单品种检查 = self.检查单品种仓位限制(品种, 引擎, 新增数量, 价格)
        检查结果["单品种"] = 单品种检查
        if not 单品种检查["允许"]:
            return {"允许": False, "原因": 单品种检查["原因"], "检查结果": 检查结果}
        
        # 4. 检查全局仓位
        新增金额 = 新增数量 * 价格
        全局仓位检查 = self.检查全局仓位限制(引擎, 新增金额)
        检查结果["全局仓位"] = 全局仓位检查
        if not 全局仓位检查["允许"]:
            return {"允许": False, "原因": 全局仓位检查["原因"], "检查结果": 检查结果}
        
        return {"允许": True, "原因": "风控检查通过", "检查结果": 检查结果}
