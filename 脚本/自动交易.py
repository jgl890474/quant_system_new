def _加载策略配置(self):
    """加载策略配置"""
    # 首先尝试从策略加载器获取实际存在的策略
    实际策略列表 = []
    if hasattr(self.策略加载器, '获取策略'):
        实际策略列表 = self.策略加载器.获取策略()
        print(f"📊 从策略加载器获取到 {len(实际策略列表)} 个策略")
    
    # 使用实际存在的策略构建配置
    if 实际策略列表:
        for s in 实际策略列表:
            策略名称 = s.get("名称", "")
            类别 = s.get("类别", "")
            品种 = s.get("品种", "")
            
            # 判断类别
            if "外汇" in 类别:
                市场类别 = "外汇"
            elif "加密" in 类别:
                市场类别 = "加密货币"
            elif "A股" in 类别:
                市场类别 = "A股"
            elif "美股" in 类别:
                市场类别 = "美股"
            else:
                市场类别 = "其他"
            
            self.策略配置列表.append({
                "名称": 策略名称,
                "类别": 市场类别,
                "启用": True,
                "品种": [品种],
                "执行间隔": 60,
                "资金分配": 0.25,
                "最大持仓": 3
            })
        print(f"✅ 从策略加载器构建 {len(self.策略配置列表)} 个策略配置")
        return
    
    # 如果策略加载器没有策略，使用默认配置
    # ... 原有代码 ...
